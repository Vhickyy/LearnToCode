// for in loop

const favColorObj = {
    Victoria: "Blue",
    Peace : "White",
    Rose: "Red",
    Jane: "Pink"
}

for (student in favColorObj){
    console.log(`${student}'s favorite color is ${favColorObj[student]}`);
}

// Victoria's favorite color is Blue
// Peace's favorite color is White
// Rose's favorite color is Red
// Jane's favorite color is Pink